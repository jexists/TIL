
basket = ['apple', 'mango', 'cherry']

# list 반복문
for fruit in basket:
    print(f'저는 {fruit}을 가지고 있습니다.')
    print('반복 예스!')  # 들여쓰기해서 반복문에 속함
print('반복 노노노')
# 저는 apple을 가지고 있습니다.
